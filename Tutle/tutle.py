import turtle

s = turtle.Screen() #abre una ventana
t = turtle.Turtle() #un puntero se habilita

t.backward(100) #una linea se realiza
t.right(90) #movimiento en grados
t.forward(100) #se mueve hacia abajo
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)

turtle.done() #se mantiene fija la ventana